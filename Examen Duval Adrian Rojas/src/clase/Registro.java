package clase;

public class Registro {
   private String TargetadeCredito;
   private String CuentaBancaria;
   private String Pasaporte;
   private String CorreoGmail;
   private String FechaNacimiento;
   
public String getTargetadeCredito() {
	return TargetadeCredito;
}
public void setTargetadeCredito(String targetadeCredito) {
	TargetadeCredito = targetadeCredito;
}
public String getCuentaBancaria() {
	return CuentaBancaria;
}
public void setCuentaBancaria(String cuentaBancaria) {
	CuentaBancaria = cuentaBancaria;
}
public String getPasaporte() {
	return Pasaporte;
}
public void setPasaporte(String pasaporte) {
	Pasaporte = pasaporte;
}
public String getCorreoGmail() {
	return CorreoGmail;
}
public void setCorreoGmail(String correoGmail) {
	CorreoGmail = correoGmail;
}
public String getFechaNacimiento() {
	return FechaNacimiento;
}
public void setFechaNacimiento(String fechaNacimiento) {
	FechaNacimiento = fechaNacimiento;
}
	
	
}
